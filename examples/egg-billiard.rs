use diffurch::{Solver, loc::detect::AboveZero};

use nalgebra::{Vector4, vector};

fn main() {
    let mut points = Vec::new();

    let mut counter = 0;

    Solver::new::<f64, Vector4<f64>>()
        .initial([0., 0.1, 0.3, 0.4])
        .equation(|s| vector![s.p[2], s.p[3], 0., 0.])
        .interval(0. ..)
        .stepsize(0.5)
        .on_mut::<AboveZero>(
            |s| {
                let v = s.p;
                v.x.powi(2) + v.y.powi(2) - v.y.powi(3) / 3. - 1.
            },
            |s| {
                let v = &mut s.p;
                let x_normal = 2. * v.x;
                let y_normal = 2. * v.y - v.y.powi(2);
                // reflection formula:
                // (*dx, *dy) changes to a vector of the same
                // magnitude and the same dot product with normal
                let h = x_normal.powi(2) + y_normal.powi(2);
                let k1 = -(x_normal.powi(2) - y_normal.powi(2)) / h;
                let k2 = -2. * x_normal * y_normal / h;
                points.push((v.x, v.y));

                (v[2], v[3]) = (k1 * v[2] + k2 * v[3], k2 * v[2] - k1 * v[3]);
                counter += 1;
                if counter >= 2000 {
                    s.stop_integration();
                }
            },
        )
        .run();

    use plotters::prelude::*;
    let root = BitMapBackend::new("egg-billiard.png", (1000, 1200)).into_drawing_area();
    root.fill(&WHITE).unwrap();
    let mut chart = ChartBuilder::on(&root)
        .margin(10)
        .build_cartesian_2d(-1. ..1., -0.9..1.35)
        .unwrap();
    chart.draw_series(LineSeries::new(points, &BLACK)).unwrap();
    root.present().unwrap();
}

